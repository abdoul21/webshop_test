<h1>politique de confidentialité</h1>
<p1> Le Règlement général sur la protection des données, communément appelé RGPD, est le texte
    européen de référence en matière deprotection desdonnées à caractère personnel. Applicable depuis le 25 mai 2018
    sur l’ensemble du territoire européen, il vientrenforcer et compléterles principes établis par d’autres lois sur
    l’informatique.
</p1>
<h2> Notion: Une donnée à caractère personne</h2>
<p1>Selon l’article 4 du RGPD (définitions), la notion de « données personnelles » est à comprendre de façon très
    large. Ce sont des données qui identifient ou rendent identifiables une personne physique, directement ou
    indirectement.L’identification d’une personne physique peut être réalisée à partir d’une seule donnée ou à
    partir du croisement d’un ensemble de données. Une personne peut être identifiée:-directement(exemple: nom,
    prénom)-ou indirectement(exemple: par un identifiant, un numéro (de téléphone), une donnée biométrique,
    plusieurs éléments spécifiques propres à son identité physique, physiologique, génétique, psychique, économique,
    culturelle ou sociale, mais aussi la voix ou l’image).
</p1>
<h2>Traitement de données personnelles</h2>
<p1>Selon l’article 4 de la RGPD (définitions), un traitement de données personnelles est une opération, ou ensemble
    d’opérations, portant sur des données personnelles, quel que soit le procédé utilisé (collecte, enregistrement
    organisation, conservation, adaptation, modification, extraction consultation, utilisation, communication par
    transmission ou diffusion ou toute autre forme de mise à disposition, rapprochement).
</p1>
<p1> Un traitement de données personnelles n’est pas nécessairement informatisé. Un traitement de données doit avoir
    un objectif, une finalité déterminée préalablement au recueil des données et à leur exploitation. Exemples de
    traitements: tenue du registre des sous-traitants, gestion des paies, gestion des ressources humaines, etc.
</p1>
<h2>les droits des personnes préalable au traitement des données </h2>
<p1>Le droit à la protection desdonnées à caractère personnelest un droit fondamental garanti par l’union européenne
    dans le cadre duRGPD.Selon la Cnil, les personnes concernées par des traitements de données personnelles
    disposent de droits leur permettant de garder la maîtrise des informations les concernant
</p1>
<h2>Le droit à l’information</h2>
<p1>Pour être loyale et licite, la collecte de données personnelles doit s’accompagner d’une information claire et
    précise des personnes sur l’identité du responsable du fichier; la finalité du traitement; les destinataires des
    données; les droits des personnes(droit d’accès, de rectification, et d’opposition), etcL’ information est
    préalable à la collecte des donnéesLe support de cette information varie en fonction des caractéristiques du
    fichier (exemple, panneau d’information pour une vidéosurveillance, mention d’information sur un formulaire,
    lecture de cette information en cas de recueil de données par téléphone.)
</p1>


